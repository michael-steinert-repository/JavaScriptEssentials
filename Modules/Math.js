/* Named Exports allows to create Functions where are multiple Values exporting from a Module */
export let add = function(n1, n2) {
    return n1 + n2;
}

export let subtract = function(n1, n2) {
    return n1 - n2;
}

export let multiply = function(n1, n2) {
    return n1 * n2;
}

export let divide = function(n1, n2) {
    return n1 / n2;
}

export let PI = 3.141;